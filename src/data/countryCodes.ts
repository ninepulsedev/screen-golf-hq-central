// 국가명 정규화 함수
export const normalizeCountryName = (country: string): string => {
  return country
    .trim()
    .toLowerCase()
    .replace(/\s+/g, '')
    .replace(/[^a-z0-9가-힣]/g, '');
};

// 국가번호 찾기 함수
export const getCountryCode = (country: string): string => {
  const normalized = normalizeCountryName(country);
  
  // 정규화된 국가명으로 검색
  for (const [countryName, countryCode] of Object.entries(COUNTRY_CODES)) {
    if (normalizeCountryName(countryName) === normalized) {
      return countryCode;
    }
  }
  
  return '+82'; // 기본값: 한국
};
